package Data;

public class DataTitle {
    private String ID;
    private String data_name;

    public void setID(String ID){
        this.ID = ID;
    }

    public void setDataName(String data_name){
        this.data_name = data_name;
    }

    public String getID(){
        return ID;
    }

    public String getDataName(){
        return data_name;
    }
}
