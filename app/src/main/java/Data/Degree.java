package Data;

public class Degree{

    private int ID;
    private String degree_name;

    public void setID(int ID){
        this.ID = ID;
    }

    public void setDegreeName(String degree_name){
        this.degree_name = degree_name;
    }

    public int getID(){
        return ID;
    }

    public String getDegreeName(){
        return degree_name;
    }

}