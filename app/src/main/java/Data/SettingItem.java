package Data;

public class SettingItem{

    private String tittle,note;

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getTittle() {
        return tittle;
    }

    public String getNote() {
        return note;
    }
}
